export function buildUserPrompt(
  articles: Array<{
    id: string;
    source: string;
    title: string;
    description: string;
    url: string;
  }>,
): string {
  const lines: string[] = [];
  lines.push(
    `以下の ${articles.length} 件の記事について、メビウス製薬視点でスコア・要約・着眼点を判定してください。\n`,
  );
  lines.push("【記事リスト（JSON）】");
  lines.push(JSON.stringify(articles, null, 2));
  return lines.join("\n");
}
